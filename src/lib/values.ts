// Function to get 3 random tasks from the task pool
export function getRandomTasks(allTasks: string[]): string[] {
  const shuffled = [...allTasks].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, 3);
}

export function getCurrentValue(): Value | null {
  const today = new Date();
  const dayOfWeek = today.getDay(); // 0 = Sunday, 1 = Monday, etc.
  
  // Convert Sunday (0) and days > 6 to null
  if (dayOfWeek === 0) {
    return null; // Sunday - no value
  }
  
  const value = values.find(v => v.dayOfWeek === dayOfWeek);
  return value || null;
}

export function getDateInfo() {
  const today = new Date();
  const days = ['Воскресенье', 'Понедельник', 'Вторник', 'Среда', 'Четверг', 'Пятница', 'Суббота'];
  const months = [
    'января', 'февраля', 'марта', 'апреля', 'мая', 'июня',
    'июля', 'августа', 'сентября', 'октября', 'ноября', 'декабря'
  ];
  
  const dayName = days[today.getDay()];
  const date = today.getDate();
  const month = months[today.getMonth()];
  const year = today.getFullYear();
  
  return {
    dayName,
    fullDate: `${date} ${month} ${year}`,
    isWeekend: today.getDay() === 0
  };
}
